//================================================================================

#include "Line.h"

//================================================================================

// CLASS - Line

//================================================================================

Line::Line() : Object( "Line" ), m_start( Vec2() ), m_end( Vec2() ), m_color( Color() ), m_thickness( 0 )
{
	//
}

//--------------------------------------------------------------------------------

void Line::onRender()
{
	glBegin( GL_LINES );

	glColor4d( m_color.r, m_color.g, m_color.b, m_color.a );

	glPointSize( m_thickness );

	glVertex2d( m_start.x, m_start.y );
	glVertex2d( m_end.x, m_end.y );

	glEnd();
}

